/* ==========================================================================
   CIR Incidents Layer
   ME Security Monitor
   --------------------------------------------------------------------------
   Source:
     data/cir-incidents.json

   Public API:
     window.createCirIncidentsLayer(map, options)

   Returned controller:
     {
       layer,
       refresh(),
       setEnabled(boolean),
       setFilters({...}),
       getState(),
       destroy()
     }

   Notes:
   - Standalone Leaflet module.
   - Does not modify the existing FIRMS layer.
   - Uses the normalized CIR JSON generated by update_cir_incidents.py.
   ========================================================================== */

(function () {
  "use strict";

  const DEFAULT_OPTIONS = {
    dataUrl: "data/cir-incidents.json",
    enabled: false,
    maxVisible: 2500,
    defaultDays: 0,
    showGraphicWarning: true,
    autoRefreshMs: 0,
  };

  function escapeHtml(value) {
    return String(value ?? "")
      .replaceAll("&", "&amp;")
      .replaceAll("<", "&lt;")
      .replaceAll(">", "&gt;")
      .replaceAll('"', "&quot;")
      .replaceAll("'", "&#039;");
  }

  function normalise(value) {
    return String(value ?? "").trim().toLowerCase();
  }

  function parseDate(value) {
    if (!value) return null;
    const date = new Date(value);
    return Number.isNaN(date.getTime()) ? null : date;
  }

  function formatDate(value) {
    const date = parseDate(value);
    if (!date) return escapeHtml(value || "—");

    return new Intl.DateTimeFormat("en-GB", {
      year: "numeric",
      month: "short",
      day: "2-digit",
      timeZone: "UTC",
    }).format(date);
  }

  function categoryKey(event) {
    return normalise(
      event.main_category ||
      event.category ||
      event.sub_category ||
      "other"
    );
  }

  function markerStyle(event) {
    const key = categoryKey(event);

    if (
      key.includes("airstrike") ||
      key.includes("air strike") ||
      key.includes("aerial")
    ) {
      return { color: "#b14c4c", fillColor: "#d65b5b", radius: 7 };
    }

    if (
      key.includes("ground") ||
      key.includes("clash") ||
      key.includes("armed")
    ) {
      return { color: "#9b6230", fillColor: "#c27a3b", radius: 7 };
    }

    if (
      key.includes("rocket") ||
      key.includes("missile") ||
      key.includes("shelling") ||
      key.includes("artillery")
    ) {
      return { color: "#80569b", fillColor: "#9c6fba", radius: 7 };
    }

    if (
      key.includes("civilian") ||
      key.includes("casual") ||
      key.includes("humanitarian")
    ) {
      return { color: "#4d7e66", fillColor: "#65a27f", radius: 6 };
    }

    if (
      key.includes("damage") ||
      key.includes("destruction") ||
      key.includes("infrastructure")
    ) {
      return { color: "#606d78", fillColor: "#7c8b97", radius: 6 };
    }

    return { color: "#3d6f8a", fillColor: "#5d8ea7", radius: 6 };
  }

  function sourceLinksHtml(links) {
    if (!Array.isArray(links) || !links.length) return "";

    const items = links
      .slice(0, 7)
      .map((url, index) => {
        const safeUrl = escapeHtml(url);
        return `<a href="${safeUrl}" target="_blank" rel="noopener noreferrer">Source ${index + 1}</a>`;
      })
      .join(" · ");

    return `
      <div style="margin-top:10px;">
        <strong>Evidence</strong><br>
        <span style="font-size:12px;">${items}</span>
      </div>
    `;
  }

  function popupHtml(event, options) {
    const category = event.main_category || event.category || "Unclassified";
    const subCategory = event.sub_category || "";
    const location = event.location || event.location_zone || "Unknown location";
    const warning =
      options.showGraphicWarning && event.graphic_warning
        ? `
          <div style="
            margin:8px 0;
            padding:7px 9px;
            border-radius:8px;
            background:rgba(177,76,76,.12);
            color:#8c3f3f;
            font-size:12px;
            font-weight:700;
          ">
            Graphic content warning
          </div>
        `
        : "";

    return `
      <div style="min-width:260px;max-width:360px;">
        <div style="font-size:15px;font-weight:800;margin-bottom:4px;">
          ${escapeHtml(location)}
        </div>

        <div style="font-size:12px;opacity:.76;margin-bottom:8px;">
          CIR verified incident · ${formatDate(event.date || event.incident_date_text)}
        </div>

        ${warning}

        <table style="width:100%;font-size:12px;border-collapse:collapse;">
          <tr>
            <td style="padding:3px 8px 3px 0;"><strong>Category</strong></td>
            <td style="padding:3px 0;">${escapeHtml(category)}</td>
          </tr>
          ${
            subCategory
              ? `
                <tr>
                  <td style="padding:3px 8px 3px 0;"><strong>Sub-category</strong></td>
                  <td style="padding:3px 0;">${escapeHtml(subCategory)}</td>
                </tr>
              `
              : ""
          }
          ${
            event.violence
              ? `
                <tr>
                  <td style="padding:3px 8px 3px 0;"><strong>Violence</strong></td>
                  <td style="padding:3px 0;">${escapeHtml(event.violence)}</td>
                </tr>
              `
              : ""
          }
          ${
            event.casualties
              ? `
                <tr>
                  <td style="padding:3px 8px 3px 0;"><strong>Casualties</strong></td>
                  <td style="padding:3px 0;">${escapeHtml(event.casualties)}</td>
                </tr>
              `
              : ""
          }
          <tr>
            <td style="padding:3px 8px 3px 0;"><strong>Coordinates</strong></td>
            <td style="padding:3px 0;">
              ${Number(event.latitude).toFixed(5)}, ${Number(event.longitude).toFixed(5)}
            </td>
          </tr>
        </table>

        ${
          event.description
            ? `
              <div style="margin-top:10px;line-height:1.45;">
                ${escapeHtml(event.description)}
              </div>
            `
            : ""
        }

        ${sourceLinksHtml(event.links)}

        <div style="margin-top:10px;font-size:11px;opacity:.68;">
          Source: Centre for Information Resilience
        </div>
      </div>
    `;
  }

  function createCirIncidentsLayer(map, userOptions = {}) {
    if (!map || typeof map.addLayer !== "function") {
      throw new Error("createCirIncidentsLayer requires a valid Leaflet map.");
    }

    const options = { ...DEFAULT_OPTIONS, ...userOptions };
    const layer = L.layerGroup();

    let events = [];
    let metadata = null;
    let enabled = Boolean(options.enabled);
    let destroyed = false;
    let timer = null;

    let filters = {
      days: Number(options.defaultDays) || 0,
      categories: new Set(),
      locations: new Set(),
      search: "",
    };

    function eventPassesFilters(event) {
      if (filters.days > 0) {
        const date = parseDate(event.date || event.incident_date_text);
        if (!date) return false;

        const cutoff = Date.now() - filters.days * 86400000;
        if (date.getTime() < cutoff) return false;
      }

      if (filters.categories.size > 0) {
        const values = [
          event.main_category,
          event.category,
          event.sub_category,
        ].map(normalise);

        const match = [...filters.categories].some((category) =>
          values.some((value) => value.includes(normalise(category)))
        );

        if (!match) return false;
      }

      if (filters.locations.size > 0) {
        const value = normalise(
          `${event.location || ""} ${event.location_zone || ""}`
        );

        const match = [...filters.locations].some((location) =>
          value.includes(normalise(location))
        );

        if (!match) return false;
      }

      if (filters.search) {
        const haystack = normalise(
          [
            event.location,
            event.location_zone,
            event.description,
            event.category,
            event.main_category,
            event.sub_category,
            event.violence,
            event.casualties,
          ].join(" ")
        );

        if (!haystack.includes(normalise(filters.search))) return false;
      }

      return true;
    }

    function render() {
      if (destroyed) return;

      layer.clearLayers();

      const visible = events
        .filter(eventPassesFilters)
        .slice(0, Math.max(1, Number(options.maxVisible) || 2500));

      for (const event of visible) {
        const latitude = Number(event.latitude);
        const longitude = Number(event.longitude);

        if (!Number.isFinite(latitude) || !Number.isFinite(longitude)) {
          continue;
        }

        const style = markerStyle(event);

        const marker = L.circleMarker([latitude, longitude], {
          radius: style.radius,
          color: style.color,
          fillColor: style.fillColor,
          fillOpacity: 0.82,
          opacity: 0.95,
          weight: 1.5,
        });

        marker.bindPopup(popupHtml(event, options), {
          maxWidth: 380,
        });

        marker.cirEvent = event;
        marker.addTo(layer);
      }

      if (enabled && !map.hasLayer(layer)) {
        layer.addTo(map);
      }

      if (!enabled && map.hasLayer(layer)) {
        map.removeLayer(layer);
      }

      return visible.length;
    }

    async function refresh() {
      if (destroyed) return 0;

      const response = await fetch(options.dataUrl, {
        cache: "no-store",
      });

      if (!response.ok) {
        throw new Error(
          `CIR incidents loading failed: HTTP ${response.status}`
        );
      }

      const payload = await response.json();
      const incoming = Array.isArray(payload)
        ? payload
        : Array.isArray(payload.events)
          ? payload.events
          : [];

      events = incoming;
      metadata = payload;

      return render();
    }

    function setEnabled(value) {
      enabled = Boolean(value);
      render();
    }

    function setFilters(nextFilters = {}) {
      if (Object.prototype.hasOwnProperty.call(nextFilters, "days")) {
        filters.days = Number(nextFilters.days) || 0;
      }

      if (Object.prototype.hasOwnProperty.call(nextFilters, "categories")) {
        filters.categories = new Set(
          Array.isArray(nextFilters.categories)
            ? nextFilters.categories
            : []
        );
      }

      if (Object.prototype.hasOwnProperty.call(nextFilters, "locations")) {
        filters.locations = new Set(
          Array.isArray(nextFilters.locations)
            ? nextFilters.locations
            : []
        );
      }

      if (Object.prototype.hasOwnProperty.call(nextFilters, "search")) {
        filters.search = String(nextFilters.search || "");
      }

      return render();
    }

    function getState() {
      const visibleCount = events.filter(eventPassesFilters).length;

      return {
        enabled,
        loadedCount: events.length,
        visibleCount,
        filters: {
          days: filters.days,
          categories: [...filters.categories],
          locations: [...filters.locations],
          search: filters.search,
        },
        generatedAt: metadata?.generated_at || null,
        status: metadata?.status || null,
        source: metadata?.source || null,
      };
    }

    function destroy() {
      destroyed = true;

      if (timer) {
        window.clearInterval(timer);
        timer = null;
      }

      if (map.hasLayer(layer)) {
        map.removeLayer(layer);
      }

      layer.clearLayers();
      events = [];
      metadata = null;
    }

    if (enabled) {
      layer.addTo(map);
    }

    if (Number(options.autoRefreshMs) > 0) {
      timer = window.setInterval(() => {
        refresh().catch((error) => {
          console.warn("[CIR] automatic refresh failed:", error);
        });
      }, Number(options.autoRefreshMs));
    }

    return {
      layer,
      refresh,
      setEnabled,
      setFilters,
      getState,
      destroy,
    };
  }

  window.createCirIncidentsLayer = createCirIncidentsLayer;
})();
